// github.com/salrashid123/signer or a similar KMS crypto.Signer adapter.
// Key material never leaves KMS; only the signing operation crosses the API.
// The JWKS must be built by calling Issuer.PublicKeySet() with the RSA public
// key obtained via kms.GetPublicKey.

// JWKSHandler returns an http.HandlerFunc that serves the public JWKS for this
// service's issuer at /.well-known/jwks.json.
//
// Recorders and Gateways fetch this endpoint to bootstrap their Verifier. The
// response is the raw JSON returned by streamclaims.Issuer.PublicKeySet().
//
// Cache-Control: max-age=300 matches the token MaxTTL so verifiers that cache
// the JWKS never miss a key for tokens that were valid when minted.
func JWKSHandler(issuer *streamclaims.Issuer) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		if r.Method != http.MethodGet {
			w.WriteHeader(http.StatusMethodNotAllowed)
			return
		}
		raw, err := issuer.PublicKeySet()
		if err != nil {
			http.Error(w, "failed to build JWKS", http.StatusInternalServerError)
			return
		}
		w.Header().Set("Content-Type", "application/json")
		w.Header().Set("Cache-Control", "public, max-age=300")
		w.WriteHeader(http.StatusOK)
		_, _ = w.Write(raw)
	}
}

// parseRSAPrivateKeyPEM attempts to decode a PEM block and parse the key as
// PKCS#1 (RSA PRIVATE KEY) or PKCS#8 (PRIVATE KEY). Returns *rsa.PrivateKey
// wrapped as crypto.Signer.
func parseRSAPrivateKeyPEM(data []byte) (crypto.Signer, error) {
	block, _ := pem.Decode(data)
	if block == nil {
		return nil, fmt.Errorf("streams: no PEM block found in key material")
	}
	switch block.Type {
	case "RSA PRIVATE KEY":
		k, err := x509.ParsePKCS1PrivateKey(block.Bytes)
		if err != nil {
			return nil, fmt.Errorf("streams: parse PKCS1 private key: %w", err)
		}
		return k, nil
	case "PRIVATE KEY":
		raw, err := x509.ParsePKCS8PrivateKey(block.Bytes)
		if err != nil {
			return nil, fmt.Errorf("streams: parse PKCS8 private key: %w", err)
		}
		k, ok := raw.(*rsa.PrivateKey)
		if !ok {
			return nil, fmt.Errorf("streams: PKCS8 key is not RSA (got %T)", raw)
		}
		return k, nil
	default:
		return nil, fmt.Errorf("streams: unsupported PEM block type %q (want RSA PRIVATE KEY or PRIVATE KEY)", block.Type)
	}
}
