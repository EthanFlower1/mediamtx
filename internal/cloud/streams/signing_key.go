// Package streams implements the cloud-side stream URL minting service (KAI-255).
//
// Architectural seam #5: token SIGNING is cloud-side only. Verification happens
// at Recorder/Gateway via the JWKS endpoint served here. Do not embed an
// issuer in any on-prem component.
package streams

import (
	"context"
	"crypto"
	"crypto/rsa"
	"crypto/x509"
	"encoding/pem"
	"fmt"
	"net/http"
	"os"

	"github.com/bluenviron/mediamtx/internal/shared/streamclaims"
)

// SigningKeySource is the seam for loading the cloud signing key. The cloud
// platform signs with RS256; the private key lives in AWS KMS in production
// and in a PEM file or env var in tests and local dev.
//
// Implementations must be safe for concurrent use.
type SigningKeySource interface {
	// LoadPrivateKey returns the signer backed by the cloud RSA private key.
	// Production: AWS KMS-backed signer (KMS never exposes the raw key bytes).
	// Test/dev: file-backed or env-backed concrete *rsa.PrivateKey.
	LoadPrivateKey(ctx context.Context) (crypto.Signer, error)
}

// FileSigningKeySource reads an RSA private key from a PEM file on disk.
// It is the test/dev/local implementation. Do not use in production — use
// KMSSigningKeySource (or its stub) once AWS KMS is wired.
type FileSigningKeySource struct {
	// Path is the filesystem path to the PKCS#8 or PKCS#1 PEM-encoded
	// RSA private key file.
	Path string
}

// LoadPrivateKey reads and parses the PEM key file.
func (f *FileSigningKeySource) LoadPrivateKey(_ context.Context) (crypto.Signer, error) {
	data, err := os.ReadFile(f.Path)
	if err != nil {
		return nil, fmt.Errorf("streams: FileSigningKeySource: read %q: %w", f.Path, err)
	}
	return parseRSAPrivateKeyPEM(data)
}

// EnvSigningKeySource reads an RSA private key from an environment variable
// whose value is a PEM block. Useful in CI environments and Docker Compose.
type EnvSigningKeySource struct {
	// EnvVar is the name of the environment variable holding the PEM key.
	EnvVar string
}

// LoadPrivateKey reads and parses the PEM key from the environment.
func (e *EnvSigningKeySource) LoadPrivateKey(_ context.Context) (crypto.Signer, error) {
	data := os.Getenv(e.EnvVar)
	if data == "" {
		return nil, fmt.Errorf("streams: EnvSigningKeySource: env var %q is empty or unset", e.EnvVar)
	}
	return parseRSAPrivateKeyPEM([]byte(data))
}

// StaticSigningKeySource holds an already-loaded signer. Used in tests to
// inject an ephemeral key generated by streamclaims.GenerateTestKey.
type StaticSigningKeySource struct {
	Key crypto.Signer
}

// LoadPrivateKey returns the static signer directly.
func (s *StaticSigningKeySource) LoadPrivateKey(_ context.Context) (crypto.Signer, error) {
	if s.Key == nil {
		return nil, fmt.Errorf("streams: StaticSigningKeySource: Key is nil")
	}
	return s.Key, nil
}

