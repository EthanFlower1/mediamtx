// pairing.proto — Recorder pairing handshake.
//
// Pairing is how a freshly-flashed Recorder box joins a Directory. The flow:
//
//   1. Admin (in the Directory UI) generates a PairingToken bound to a
//      tenant + suggested recorder mode. The token carries everything the
//      recorder needs to bootstrap:
//        - Directory endpoint URL + fingerprint
//        - Headscale pre-auth key (for the control-plane overlay)
//        - Step-CA fingerprint + single-use enrol token (for mTLS identity)
//   2. Admin runs `kaivue-recorder pair <token>` on the recorder box.
//   3. Recorder enrolls with Headscale + Step-CA, then dials Directory.
//   4. First call is CompletePairingRequest: recorder proves its new cert
//      and is marked "paired" in Directory's recorder registry.
//   5. PairingToken is burned (single-use) regardless of outcome.
//
// The PairingToken itself is a short-lived JWT signed by Directory. This
// .proto describes the DECODED claim set so Go / Dart consumers don't have
// to re-implement the claim structure.

