// RecorderControl is a server-streaming RPC that the Directory uses to push
// camera assignments (and revocations, and config updates) to a connected
// Recorder. The Recorder is the CLIENT — it dials Directory, authenticates
// with its recorder identity cert, and opens a long-lived stream.
//
// Lifecycle:
//   1. Recorder dials StreamAssignments with its current known_version.
//   2. Directory immediately sends a Snapshot event containing all cameras
//      currently assigned to this recorder. The snapshot carries the
//      server's latest version number.
//   3. Directory then pushes CameraAdded / CameraUpdated / CameraRemoved
//      events as state changes, each carrying the new version.
//   4. If the connection drops and resumes, the recorder passes its last
//      known version; Directory decides whether to send a delta or a fresh
//      Snapshot.
//
// Back-pressure: Directory buffers a bounded queue per recorder. If the
// recorder can't drain fast enough, Directory logs and sends a Snapshot on
